    //Metodo boton iniciar
    private void btn_encenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_encenderActionPerformed
        
        textoDeBotonesCuandoCargaCamara();
        
        Thread hilo = new Thread(){
          
            @Override
            public void run(){
                webcamPanel.start();
                prenderBotones();
                textoDeBotonesCuandoCargoCamara();
            }
        };
        
        hilo.setDaemon(true);
        hilo.start();
        btn_encender.setEnabled(false);
        
    }//GEN-LAST:event_btn_encenderActionPerformed

